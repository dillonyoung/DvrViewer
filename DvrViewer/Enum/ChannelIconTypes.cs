namespace DvrViewer.Enum
{
    /// <summary>
    /// Represents the possible channel icons
    /// </summary>
    public enum ChannelIconTypes
    {
        /// <summary>
        /// The icon for the channel is the camera icon
        /// </summary>
        Camera = 1,

        /// <summary>
        /// The icon for the channel is the disabled camera icon
        /// </summary>
        CameraDisabled = 2,

        /// <summary>
        /// The icon for the channel is the playing icon
        /// </summary>
        Playing = 3,

        /// <summary>
        /// The icon for the channel is the stopped icon
        /// </summary>
        Stopped = 4
    }
}
