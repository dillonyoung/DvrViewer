namespace DvrViewer.Enum
{
    /// <summary>
    /// Represents the possible video statuses
    /// </summary>
    public enum VideoStatuses
    {
        /// <summary>
        /// The video is currently stopped
        /// </summary>
        Stopped = 1,

        /// <summary>
        /// The video is currently playing
        /// </summary>
        Playing = 2,

        /// <summary>
        /// The video is currently paused
        /// </summary>
        Paused = 3
    }
}
